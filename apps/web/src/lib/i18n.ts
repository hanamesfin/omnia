const DICTS: Record<LocaleId, Dict> = {
  en,
  es,
  fr,
  de,
  pt,
  it,
  nl,
  tr,
  ar,
  hi,
  zh,
  ja,
  ko,
  ru,
};

export function isLocaleId(v: string | null | undefined): v is LocaleId {
  return !!v && v in DICTS;
}

export function getLocaleMeta(id: LocaleId): LocaleMeta {
  return LOCALES.find((l) => l.id === id) || LOCALES[0];
}

export function translate(locale: LocaleId, key: string): string {
  return DICTS[locale]?.[key] ?? en[key] ?? key;
}

export function detectBrowserLocale(): LocaleId {
  if (typeof navigator === "undefined") return "en";
  const nav = (navigator.language || "en").toLowerCase();
  const exact = LOCALES.find((l) => nav === l.id || nav.startsWith(`${l.id}-`));
  if (exact) return exact.id;
  // Chinese variants
  if (nav.startsWith("zh")) return "zh";
  return "en";
}
